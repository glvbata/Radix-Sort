package util;

/**
 * An interface that has the method that enables a enqueue to enqueue at the
 * front.
 * 
 * @author Gian Lendl V. Bata
 * @version v1.0
 * @param <E> A generic type.
 */
public interface OutputRestrictedDoubleEndedQueue<E> extends Queue<E> {
  
  /**
   * Adds an element in the front of the queue.
   * 
   * @param the_element the element.
   */
  void enqueueAtFront(E the_element);
}
